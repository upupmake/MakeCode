VALID_STATUS = {
    "pending",
    "in_progress",
    "completed",
}


class CreateTask(BaseModel):
    """
    Create one new task.
    Use this when you want to append a single executable unit to the topology plan.
    `task_id` is auto-generated by TaskManager; do not provide `task_id` in input.
    Constraints:
    - `subject` is required and cannot be empty.
    - `depend_on` can only contain existing task IDs.
    - MUST NOT create tasks that edit the same file without topology ordering — concurrent writes to the same file cause conflicts.
    - If multiple tasks need to edit the same file, you MUST establish explicit topology dependencies (via `depend_on`) so that they execute sequentially.
    - Task will be topology-validated after insertion.
    """

    subject: str = Field(
        ..., min_length=1, description="Task title, concise and action-oriented."
    )
    description: str = Field(
        default="", description="Optional detailed description for the task."
    )
    depend_on: list[str] = Field(
        default_factory=list, description="IDs of tasks this task depends on."
    )
    status: Literal["pending", "in_progress", "completed"] = Field(
        default="pending", description="Initial task status."
    )

    @field_validator("depend_on", mode="before")
    @classmethod
    def parse_stringified_deps(cls, v: Any) -> Any:
        if isinstance(v, str):
            try:
                v = v.strip()
                if not v:
                    return v
                parsed = json.loads(v)
                if isinstance(parsed, list):
                    v = parsed
                else:
                    return v
            except json.JSONDecodeError:
                pass

        if isinstance(v, list):
            res = []
            for item in v:
                if isinstance(item, str):
                    try:
                        item_parsed = json.loads(item.strip())
                        if isinstance(item_parsed, dict):
                            item = item_parsed
                    except json.JSONDecodeError:
                        pass

                if isinstance(item, dict):
                    if "task_id" in item:
                        res.append(str(item["task_id"]))
                    elif "id" in item:
                        res.append(str(item["id"]))
                    else:
                        res.append(str(item))
                elif item is not None:
                    res.append(str(item))
            return res
        return v


